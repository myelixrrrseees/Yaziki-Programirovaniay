let array = [9, 7, 5, 3, 1];

// Функция для проверки упорядоченности массива по убыванию
function isDescending(arr) {
    for (let i = 0; i < arr.length - 1; i++) {
        if (arr[i] < arr[i + 1]) {
            return i; // Возвращает индекс первого элемента, нарушающего упорядоченность
        }
    }
    return -1; // Возвращает -1, если массив упорядочен по убыванию
}

// Проверка массива и вывод результата
let result = isDescending(array);

if (result === -1) {
    // Если массив упорядочен по убыванию, вывести его в обратном порядке
    console.log("Массив упорядочен по убыванию. Обратный порядок:", array.reverse());
} else {
    // Если массив не упорядочен по убыванию, вывести индекс первого элемента, нарушающего упорядоченность
    console.log("Массив не упорядочен по убыванию. Индекс первого элемента, нарушающего упорядоченность:", result);
}