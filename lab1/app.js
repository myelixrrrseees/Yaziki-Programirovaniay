/*3. Количество ворон на ветке. В зависимости от введенного числа (не более 10),
выводить сообщение:
- На ветке сидит 1 ворона
- На ветке сидит 4 вороны
- На ветке сидит 10 ворон
Рекомендации: в зависимости от введенного числа, меняется окончание у слова
"ворона". Для проверки используйте оператор Switch.*/

// Вводим количество ворон
const readline = require('readline');

// Создаем интерфейс для ввода
const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

// Запрашиваем у пользователя количество ворон
rl.question('Введите количество ворон (от 1 до 10): ', (numberOfCrows) => {
    // Преобразуем введенное значение в целое число
    numberOfCrows = parseInt(numberOfCrows);

switch (numberOfCrows) {
    case 1:
        console.log("На ветке сидит 1 ворона");
        break;
    case 2:
    case 3:
    case 4:
        console.log("На ветке сидит " + numberOfCrows + " вороны");
        break;
    case 5:
    case 6:
    case 7:
    case 8:
    case 9:
    case 10:
        console.log("На ветке сидит " + numberOfCrows + " ворон");
        break;
    default:
        console.log("Пожалуйста, введите число от 1 до 10");
}
rl.close();
});

